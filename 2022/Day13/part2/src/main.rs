#[derive(Debug)]
enum Packet {
    Many(Box<Vec<Packet>>),
    Single(i32)
}


fn get_packet(chars:&mut Vec<char>) -> Packet {
    let mut curr_packet:Box<Vec<Packet>> = Box::new(Vec::<Packet>::new());
    //let mut curr_packet = Vec::<Packet>::new();
    let mut curr_num:i32 = 0;
    let mut on_number = false; 
    while chars.len() > 0 {
        let next_char = chars.pop().unwrap();
        //println!("Char: {}", next_char);
        if next_char == ','{
            if on_number {
                curr_packet.push(Packet::Single(curr_num));
            }
            curr_num = 0;
            on_number = false;
        }
        else if next_char == ']'{
            if on_number{
                curr_packet.push(Packet::Single(curr_num));
            }
            return Packet::Many(curr_packet)
        }
        else if next_char == '['{
            let term:Packet = get_packet(chars);
            curr_packet.push(term);
        }
        //else its an integer, we add it to current number
        else {
            let converted_num:i32 = next_char.to_digit(10).expect("Unable to parse char").try_into().unwrap();
            curr_num = 10*curr_num + converted_num;
            on_number = true;
        }
    }
    println!("Error did not encounter terminal ]");
    Packet::Single(0)
}


fn compare_packets(a:&Packet, b:&Packet) -> std::cmp::Ordering {
    let val = check_packets(a,b);
    if val < 0 {
        return std::cmp::Ordering::Greater
    }
    if val > 0 {
        return std::cmp::Ordering::Less
    }
    std::cmp::Ordering::Equal
}

fn handle_input(input:&str){
    let mut packets:Vec<Packet> = Vec::<Packet>::new();
    //read input
    for line in input.split("\n"){
        if line.len() == 0{
            continue;
        }
        let mut char_vec:Vec<char> = line.chars().into_iter().rev().collect();
        char_vec.pop();
        let curr_packet:Packet = get_packet(&mut char_vec);
        packets.push(curr_packet);
    }
    //add divider packets
    let two:&str = "[[2]]";
    let mut two_char_vec:Vec<char> = two.chars().into_iter().rev().collect();
    two_char_vec.pop();
    packets.push(get_packet(&mut two_char_vec));
    let six:&str = "[[6]]";
    let mut six_char_vec:Vec<char> = six.chars().into_iter().rev().collect();
    six_char_vec.pop();
    packets.push(get_packet(&mut six_char_vec));
    
    //sort
    packets.sort_by(|a, b| compare_packets(&a,&b));
    
    //score
    let mut score:i32 = 1;
    let mut cnt:i32 = 1;
    for p in packets {
        //println!("{:?}", p);
        if is_divisor(&p) {
            score *=cnt;
        }
        cnt+=1;
    }
    println!("Found divisor score of {}", score);
    println!("Done processing");
}

fn is_divisor(p:&Packet) -> bool {
    match p{
        Packet::Many(tmp) => {
            if tmp.len() != 1{
                return false
            }
            match tmp.get(0).unwrap() {
                Packet::Many(sub_tmp) =>{
                    if sub_tmp.len() != 1{
                        return false
                    }
                    match sub_tmp.get(0).unwrap() {
                        Packet::Single(val) => {
                            if *val == 2 || *val ==6{
                                return true
                            }
                            return false
                        },
                        _ => return false
                    }
                },
                _ => return false
            }

        },
        _ => return false
    }
}


//-1 means out of order, 1 means order, 0 means check next
fn check_packets(p_a:&Packet, p_b:&Packet) -> i32 {
    match (p_a,p_b) {
        (Packet::Single(A), Packet::Single(B)) => {
            if A < B {
                return 1
            }
            if B < A {
                return -1
            }
            return 0
        },
        (Packet::Many(A), Packet::Many(B)) => {
            for i in 0..A.len(){
                if i>=B.len(){
                    return -1
                }
                let comp = check_packets(A.get(i).unwrap(), B.get(i).unwrap());
                if comp != 0 {
                    return comp
                }
            }
            if B.len()>A.len(){
                return 1
            }
            return 0
        },
        (Packet::Many(A),Packet::Single(B)) => {
            let mut list_packet:Box<Vec<Packet>> = Box::new(Vec::<Packet>::new());
            list_packet.push(Packet::Single(*B));
            return check_packets(p_a, &Packet::Many(list_packet))

        },
        (Packet::Single(A),Packet::Many(B)) => {
            let mut list_packet:Box<Vec<Packet>> = Box::new(Vec::<Packet>::new());
            list_packet.push(Packet::Single(*A));
            return check_packets(&Packet::Many(list_packet), p_b)
        }
    }
}

